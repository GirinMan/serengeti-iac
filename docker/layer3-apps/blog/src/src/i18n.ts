type UiKeys = keyof (typeof ui)["en"];

export function t(locale: Locale, key: UiKeys): string {
  return ui[locale][key] ?? ui[defaultLocale][key] ?? key;
}

export function getLocaleFromUrl(url: URL): Locale {
  const segments = url.pathname.split("/").filter(Boolean);
  // After base path removal, check if first segment is a locale
  const base = import.meta.env.BASE_URL.replace(/^\/|\/$/g, "");
  const idx = base ? segments.indexOf(base.split("/").pop()!) + 1 : 0;
  const candidate = segments[idx];
  if (candidate === "en") return "en";
  return "ko";
}

export function localePath(locale: Locale, path: string): string {
  const base = import.meta.env.BASE_URL.replace(/\/$/, "");
  if (locale === "ko") return `${base}${path}`;
  return `${base}/en${path}`;
}

export function getAlternateLocale(locale: Locale): Locale {
  return locale === "en" ? "ko" : "en";
}
